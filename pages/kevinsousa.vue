        <h1>Kevin Sousa</h1>
        <p>I grew up in McKees Rocks, Pennsylvania; quintessential blue­-collar Pittsburgh. Two generations of my family operated a Mom and Pop restaurant in the downtown ­shopping district. I grew up around food.</p>
        <p>Even when we had little else, we had access to good food. When the regional economy collapsed, and the steel industry shed some 250,000 jobs, McKees Rocks suffered much in the same way Braddock did. When the jobs left town, the business district along with the restaurants weren't far behind.</p>
        <p>After culinary school, I had the good fortune of working under great chefs who imparted a new respect for food and community. I also started to develop some of my own philosophies about restaurants. I started to notice that good, conscientious restaurants did more than just fill stomachs.</p>
        <p>Good restaurants act on the opportunity to bring communities together in very unique and instinctual ways.</p>
        <p>Everyone knows that good food has soul. However, I quickly discovered that not every restaurant has soul.</p>
        <p>The seasons, food traditions - old and new, ingredients, solid technique and mentoring are core tenants of transformative cooking.</p>
        <p>A great restaurant takes much more than one individual. A great restaurant needs a community that is willing to support and grow with it over time.</p>
        <p>When I was first introduced to Braddock, I immediately felt a very deep connection to the town and imagined what the downtown must have looked like in its prime. The place just struck a chord that continues to resonate because of where and how I grew up.</p>
